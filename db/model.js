const mongoose = require('mongoose');

const { Schema } = mongoose;

const mySchema = new Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
});

const model = mongoose.model('Users', mySchema);
module.exports = model;
